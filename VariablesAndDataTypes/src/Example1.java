public class Example1 {

    // global variable
     static int int_variable=5678;
     static long long_variable=845648564;
     static short short_variable=32764;
     static boolean bool_variable=true;
     static byte byte_variable=125;
     static double doub_variable=123.5656;
     static float float_variable=1.8454565f;

     public static void main(String[] args){
         System.out.println(int_variable);
         System.out.println(doub_variable);
         System.out.println(float_variable);
         System.out.println(byte_variable);
         System.out.println(long_variable);
         System.out.println(short_variable);
         System.out.println(bool_variable);
     }


}
